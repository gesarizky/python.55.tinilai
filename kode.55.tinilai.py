# Menginput
y1 = int(input("Masukan Nilai Y1 : "))
y2 = int(input("Masukan Nilai Y2: "))
x1 = int(input("Masukan Nilai X1: "))

# Mengkonversi
senilai = x1 * y2 / y1
tinilai = x1 * y1 / y2

# Menampilkan Hasil
print('==========================================================')
print('Maka Hasil Senilai : Y1/Y2 = X1/X2 =',senilai)
print('Maka Hasil Berbalik Nilai : Y1/Y2 = X2/X1 =',tinilai)
print('==========================================================')
